


public class Encapsulation1 {

	private String name;
	private String breed;
	private int cost;
	
	public void setName(String name) {
		this.name= name;
	}
	public void setBreed(String breed) {
		this.breed = breed;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public String getName() {
		return name; 
	}
	public String getBreed() {
		return breed; 
	}
	public int getCost() {
		return cost;
	}
}
